//// WAP to print first N even numbers

package DAY2;
import java.util.Scanner;
public class lec3 {
    public static void main(String[] args){
        Scanner sc= new Scanner(System.in);
        int n=sc.nextInt();
        for(int i=2;i<=n;i=i+2){
            System.out.println(i);

        }



    }
}
